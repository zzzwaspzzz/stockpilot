    public void cargar_componentes_cmbox_tipoart() {
        cmbox_tipo_articulo.removeAllItems();
        //cmbox_tipo_articulo.addItem("Seleccione tipo...");

    try {
        BBDD bd = new BBDD();
        List<Articulo> articulos = bd.listar_articulos();        

        if (articulos != null) {
            for (Articulo art : articulos) {
                if (art != null && !art.getNombreArt().isEmpty()) {
                    cmbox_tipo_articulo.addItem(art);
                }
            }
        }
    } catch (Exception e) {
        System.err.println("Error al cargar artículos: " + e.getMessage());        
        }
    }    
    
    public void cargar_proveedores_jdialog(){
    
       cmb_nuevoart_proveedor.removeAllItems();
       BBDD bd = new BBDD();
       proveedores = bd.obtener_proveedores();       
       
       if(proveedores != null){
           cmb_nuevoart_proveedor.addItem("Seleccione proveedor...");
           for (Proveedor proveedor : proveedores)
           {
               cmb_nuevoart_proveedor.addItem(proveedor.getNombreProov());
           }
       }
        
    }  

