    private void btn_nuevoart_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_nuevoart_aceptarActionPerformed
        String nombre_articulo = txtNuevoArticulo.getText().trim();

        if (nombre_articulo.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El nombre del artículo no puede estar vacío.");
            return;
        }

        String item = cmb_nuevoart_proveedor.getSelectedItem().toString();

        if (item.equals("Seleccione proveedor...")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar un proveedor.");
            return;
        }

        Proveedor proveedor = null;

        for (Proveedor p : proveedores) {
            if (p.getNombreProov().equals(item)) {
                proveedor = p;
                break;
            }
        }

        if (proveedor == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Proveedor no encontrado.");
            return;
        }

        // =========================
        // ERP N:M REAL
        // =========================
        Articulo art = new Articulo();
        art.setNombreArt(nombre_articulo);

        ArticuloProveedor ap = new ArticuloProveedor();
        ap.setArticulo(art);
        ap.setProveedor(proveedor);

        java.util.Set<ArticuloProveedor> set = new java.util.HashSet<>();
        set.add(ap);

        art.setArticuloProveedors(set);

        BBDD bd = new BBDD();

        boolean ok = bd.insertar_articulo(art);

        if (ok) {
            javax.swing.JOptionPane.showMessageDialog(this, "Artículo creado correctamente.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar artículo.");
        }

        cargar_componentes_cmbox_tipoart();
    }//GEN-LAST:event_btn_nuevoart_aceptarActionPerformed

